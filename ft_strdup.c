#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*temp;
	int		i;

	if (!(temp = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
